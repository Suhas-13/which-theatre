class Show:
  def __init__(self, movie, theatre, theatre_chain, has_subtitles, subtitles_language, show_date, start_time, run_time):
    self.movie = movie
    self.theatre = theatre
    self.theatre_chain = theatre_chain
    self.has_subtitles = has_subtitles
    self.subtitles_language = subtitles_language
    self.show_date = show_date
    self.start_time = start_time
    self.run_time = run_time